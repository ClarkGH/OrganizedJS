// Functional Programming

/* 
  Functional programming utilizes mathematical functions
  It:
    - Avoids shared state
    - Avoids Mutability
    - Uses PURE functions
    - Avoids side-effects
    - Is simple in composition
    - Uses first class functions, higher order functions, and functions as arguments
*/

/* 
  To understand functional programming, make sure you understand the following:
    - Closures
    - Callbacks
*/