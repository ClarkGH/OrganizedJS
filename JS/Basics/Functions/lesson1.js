// Functions: the basics

/*
  A function is a reusable type of object that you can call over and over
  Functions:
    - Run when called (invoked)
    - Are designed to perform a specific task
    - Have different types of uses
*/

// Example 1: Basic Function
function add1 ( num ) { //function *name* ( *param* ) { code }
  return num++; // here we're incrementing the num param by 1
}
// function invocation is: *name*(*paramVal*);
add1( 1 ); // returns 2
add1( 2 ); // returns 3

