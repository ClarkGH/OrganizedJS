// Operators and Operands

/*
  In javascript, operators are special functions that work in their own ways
  Think of operators as symbols that do specific tasks (special functions)
  Think of operands as the things operands do things with (parameters)
*/

// Example 1: basic operator and operands
1 + 2; // in this case, 1 and 2 are operands. The '+' symbol is the operator

