// Primitives

/*
 * Primitives are basic values in JavaScript.
 * They are NOT objects
 * JavaScript currently has 7 primitives.
 * 1. numbers
 * 2. strings
 * 3. booleans
 * 4. symbols
 * 5. undefined
 * 6. null
 * 7. bigInt
*/

1; // number
"use strict"; //string
true; // false
Symbol('foo'); //symbol
undefined; //undefined
null; //null
